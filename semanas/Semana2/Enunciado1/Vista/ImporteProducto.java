    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        try {
            double precio = Double.parseDouble(txtPrecio.getText());
            int cantidad = Integer.parseInt(txtCantidad.getText());

            if (precio < 0 || cantidad < 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Ingrese valores positivos", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Importe de compra (precio * cantidad)
            double importeCompra = precio * cantidad;

            // Calcular descuento
            double descuento;
            if (cantidad >= 10) {
                descuento = 0.20 * importeCompra;
            } else {
                descuento = 0.10 * importeCompra;
            }

            // Importe a pagar después del descuento
            double importePagar = importeCompra - descuento;

            // Calcular lapiceros
            int lapiceros = (importePagar >= 200) ? 2 * cantidad : 0;

            // Mostrar resultados
            txtSalida.setText("");
            txtSalida.append(String.format("Importe de compra: S/. %.2f\n", importeCompra));
            txtSalida.append(String.format("Importe del descuento: S/. %.2f\n", descuento));
            txtSalida.append(String.format("Importe a pagar: S/. %.2f\n", importePagar));
            txtSalida.append("Cantidad de lapiceros de obsequio: " + lapiceros + "\n");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese números válidos", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

