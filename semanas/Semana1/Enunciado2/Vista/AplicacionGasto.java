    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        try {
            double montoTotal = Double.parseDouble(txtMonto.getText().trim());

        if (montoTotal <= 0) {
            JOptionPane.showMessageDialog(this, "Ingrese un monto mayor a cero");
            return;
        }

        double alquiler = montoTotal * 0.23;
        double publicidad = montoTotal * 0.07;
        double transporte = montoTotal * 0.26;
        double servicios = montoTotal * 0.12;
        double decoracion = montoTotal * 0.21;
        double gastosVarios = montoTotal * 0.11;

        StringBuilder sb = new StringBuilder();
        sb.append("Distribución de gastos para un monto total de: $")
            .append(String.format("%.2f", montoTotal))
            .append("\n----------------------------------------\n");
        sb.append(String.format("Alquiler de espacio:       $%,10.2f\n", alquiler));
        sb.append(String.format("Publicidad:                $%,10.2f\n", publicidad));
        sb.append(String.format("Transporte:                $%,10.2f\n", transporte));
        sb.append(String.format("Servicios feriales:        $%,10.2f\n", servicios));
        sb.append(String.format("Decoración:                $%,10.2f\n", decoracion));
        sb.append(String.format("Gastos varios:             $%,10.2f\n", gastosVarios));
        sb.append("----------------------------------------\n");

        txtSalidas.setText(sb.toString());

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese un valor numérico válido");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

